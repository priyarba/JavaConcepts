package javaSessions;

public class P6_MathematicalOperations {

	public static void main(String[] args) {

		System.out.println(9/2);//4 only integer output
		System.out.println(9.0/2);//4.5
		System.out.println(9/2.0);//4.5
		
		//System.out.println(9/0);//Arithmetic Exception
		System.out.println(0/9);//0
		
		System.out.println(9.0/0);//Infinity
		System.out.println(9/0.0);//Infinity
		System.out.println(9.0/0.0);//Infinity
		
		//System.out.println(0/0);//Arithmetic exception
		
		System.out.println(2/9);//0
		System.out.println(0.0/0.0);//NaN : not a number
		System.out.println(0/0.0);//NaN
		System.out.println(0.0/0);//NaN
		
		//System.out.println(-12/0);//Arithmetic exception
		
		System.out.println(0/9.0);//0.0
		
		System.out.println(0.1+0.2);//0.30000000000000004
		System.out.println(0.1+0.4);//0.5
		
		
		
		
		
	}

}
